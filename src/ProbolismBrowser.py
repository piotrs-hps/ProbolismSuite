    def clear_browser_cache(self) -> int:
        """Delete only files created by this browser, not arbitrary user files."""
        cache_dir = Path(self.cache_dir_var.get().strip().strip('"') or DEFAULT_CACHE_DIR)
        if not cache_dir.exists() or not cache_dir.is_dir():
            return 0
        patterns = [
            "probolism_*.jpg",
            "probolism_*.jpeg",
            "probolism_*.png",
            "probolism_*.json",
            "probolism_*.tmp",
            "probolism_*.jpg.tmp",
            "galaxy_name_resolutions.json",
        ]
        deleted = 0
        for pattern in patterns:
            for path in cache_dir.glob(pattern):
                try:
                    if path.is_file():
                        path.unlink()
                        deleted += 1
                    elif path.is_dir():
                        shutil.rmtree(path)
                        deleted += 1
                except Exception:
                    pass
        return deleted

