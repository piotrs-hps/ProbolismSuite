def _dependency_error(missing_packages: List[str], details: str = "") -> None:
    unique: List[str] = []
    for pkg in missing_packages:
        if pkg not in unique:
            unique.append(pkg)
    install_cmd = f'"{sys.executable}" -m pip install --upgrade ' + " ".join(unique)
    msg = (
        "Required Python packages are missing:\n\n"
        + "\n".join(f"  • {pkg}" for pkg in unique)
        + "\n\nInstall them with this command in PowerShell / Terminal:\n\n"
        + install_cmd
    )
    if details:
        msg += "\n\nTechnical details:\n" + details
    try:
        import tkinter as _tk
        from tkinter import messagebox as _messagebox
        _root = _tk.Tk()
        _root.withdraw()
        _messagebox.showerror("Missing Python packages", msg)
        _root.destroy()
    except Exception:
        pass
    print(msg, file=sys.stderr)
    raise SystemExit(1)


