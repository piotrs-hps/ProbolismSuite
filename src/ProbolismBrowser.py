    def _image_result_for_export(
        self,
        index: int,
        row: pd.Series,
        selected_fov: float,
        survey: str,
        auto_fit: bool,
        cache_dir: Path,
        width: int,
    ) -> ImageResult:
        ra_deg, dec_deg = row_to_center_degrees(row)
        displayed_fov = selected_fov + DISPLAY_MARGIN_DEG
        auto_fov_used = False
        resolved: List[Tuple[str, float, float, float]] = []
        failed: List[str] = []
        cache_dir.mkdir(parents=True, exist_ok=True)

        galaxy_names = split_galaxy_names(row.get("galaxies", ""))
        if galaxy_names:
            resolver = GalaxyResolver(cache_dir)
            for name in galaxy_names:
                coords = resolver.resolve(name)
                if coords is None:
                    failed.append(name)
                    continue
                sep = angular_separation_deg(ra_deg, dec_deg, coords[0], coords[1])
                resolved.append((name, coords[0], coords[1], sep))

        nearest_galaxy_name: Optional[str] = None
        nearest_galaxy_sep_deg: Optional[float] = None
        if resolved:
            nearest = min(resolved, key=lambda item: item[3])
            nearest_galaxy_name = nearest[0]
            nearest_galaxy_sep_deg = nearest[3]

        if auto_fit and resolved:
            max_sep = max(item[3] for item in resolved)
            needed = 2.0 * (max_sep + AUTO_GALAXY_MARGIN_DEG)
            if needed > displayed_fov:
                displayed_fov = needed
                auto_fov_used = True

        height = width
        payload = {
            "service": "hips2fits",
            "hips": survey,
            "ra_deg": round(ra_deg, 7),
            "dec_deg": round(dec_deg, 7),
            "fov_deg": round(displayed_fov, 5),
            "width": width,
            "height": height,
            "projection": "TAN",
            "format": "jpg",
            "stretch": "asinh",
            "record_index": index + 1,
        }
        cache_key = safe_filename_hash(payload)
        image_path = cache_dir / f"probolism_{cache_key}.jpg"
        meta_path = cache_dir / f"probolism_{cache_key}.json"
        image_url = build_hips2fits_url(HIPS2FITS_PRIMARY, survey, ra_deg, dec_deg, displayed_fov, width, height, "jpg")
        cache_hit = is_valid_image_file(image_path)

        if not cache_hit:
            if image_path.exists():
                try:
                    image_path.unlink()
                except Exception:
                    pass
            mirror_url = build_hips2fits_url(HIPS2FITS_MIRROR, survey, ra_deg, dec_deg, displayed_fov, width, height, "jpg")
            image_url = fetch_image_to_cache_with_retries(image_url, mirror_url, image_path)
            meta_path.write_text(
                json.dumps({**payload, "url": image_url, "created_at": time.strftime("%Y-%m-%dT%H:%M:%S")}, indent=2),
                encoding="utf-8",
            )

        return ImageResult(
            index=index,
            token=0,
            image_path=image_path,
            cache_hit=cache_hit,
            ra_deg=ra_deg,
            dec_deg=dec_deg,
            selected_fov_deg=selected_fov,
            displayed_fov_deg=displayed_fov,
            auto_fov_used=auto_fov_used,
            resolved_galaxies=resolved,
            failed_galaxies=failed,
            nearest_galaxy_name=nearest_galaxy_name,
            nearest_galaxy_sep_deg=nearest_galaxy_sep_deg,
            image_url=image_url,
        )

    @staticmethod
    def _export_font() -> ImageFont.ImageFont:
        for name in ("arial.ttf", "segoeui.ttf", "DejaVuSans.ttf"):
            try:
                return ImageFont.truetype(name, 16)
            except Exception:
                pass
        return ImageFont.load_default()

    @staticmethod
    def _wrap_text_for_export(text: str, width_chars: int = 68) -> List[str]:
        wrapped: List[str] = []
        for raw_line in text.splitlines():
            if not raw_line:
                wrapped.append("")
                continue
            indent = len(raw_line) - len(raw_line.lstrip(" "))
            prefix = " " * indent
            words = raw_line.split(" ")
            line = ""
            for word in words:
                candidate = word if not line else line + " " + word
                if len(candidate) <= width_chars:
                    line = candidate
                else:
                    if line:
                        wrapped.append(line)
                    line = prefix + word if prefix and not word.startswith(prefix) else word
            if line:
                wrapped.append(line)
        return wrapped

    def _build_export_record_block(self, image: Image.Image, details: str, record_number: int) -> Image.Image:
        font = self._export_font()
        line_height = 21
        margin = 18
        gap = 18
        image_width = 420
        if image.width != image_width:
            ratio = image_width / float(image.width)
            image = image.resize((image_width, max(1, int(image.height * ratio))), Image.LANCZOS)
        text_lines = self._wrap_text_for_export(details, width_chars=72)
        text_width = 630
        text_height = max(image.height, margin * 2 + line_height * (len(text_lines) + 1))
        block = Image.new("RGB", (margin * 2 + image_width + gap + text_width, text_height), "white")
        draw = ImageDraw.Draw(block)
        block.paste(image, (margin, margin))
        x = margin + image_width + gap
        y = margin
        draw.text((x, y), f"Selected probolism record {record_number}", fill="black", font=font)
        y += line_height + 8
        for line in text_lines:
            safe_line = line.replace("—", "-").replace("×", "x").replace("°", " deg").replace("•", "*")
            draw.text((x, y), safe_line, fill="black", font=font)
            y += line_height
        return block

    def _worker_export_selected_records_to_jpg(
        self,
        records: pd.DataFrame,
        indices: List[int],
        selected_fov: float,
        survey: str,
        auto_fit: bool,
        cache_dir: Path,
        width: int,
        out_path: Path,
    ) -> None:
        blocks: List[Tuple[Image.Image, int]] = []
        saved_paths: List[Path] = []
        try:
            total = len(indices)
            for done, index in enumerate(indices, start=1):
                self.export_progress_queue.put({"kind": "progress", "done": done, "total": total, "record": index + 1})
                row = records.iloc[index]
                result = self._image_result_for_export(index, row, selected_fov, survey, auto_fit, cache_dir, width)
                img = Image.open(result.image_path).convert("RGB")
                img = self._draw_overlays(img, result, draw_galaxy_labels=auto_fit)
                nearest_info = self.format_nearest_galaxy(result, row)
                galaxy_count = len(split_galaxy_names(row.get("galaxies", "")))
                details = self.format_record_details(row, nearest_info, record_index=index, galaxies_inline=(galaxy_count > 5))
                blocks.append((self._build_export_record_block(img, details, index + 1), galaxy_count))

            if not blocks:
                raise RuntimeError("No record blocks were created.")
            spacing = 24
            out_path.parent.mkdir(parents=True, exist_ok=True)
            stem = out_path.stem or "selected_probolisms"
            suffix = out_path.suffix if out_path.suffix.lower() in (".jpg", ".jpeg") else ".jpg"
            csv_path = out_path.with_name(f"{stem}.csv")
            records.iloc[indices].to_csv(csv_path, index=False, encoding="utf-8-sig")

            groups: List[List[Image.Image]] = []
            current_group: List[Image.Image] = []
            for block, galaxy_count in blocks:
                long_galaxy_list = galaxy_count > 5
                if long_galaxy_list:
                    if current_group:
                        groups.append(current_group)
                        current_group = []
                    groups.append([block])
                else:
                    current_group.append(block)
                    if len(current_group) == 2:
                        groups.append(current_group)
                        current_group = []
            if current_group:
                groups.append(current_group)

            for file_number, group in enumerate(groups, start=1):
                final_width = max(block.width for block in group)
                final_height = sum(block.height for block in group) + spacing * (len(group) - 1)
                final_img = Image.new("RGB", (final_width, final_height), "white")
                y = 0
                for block in group:
                    final_img.paste(block, (0, y))
                    y += block.height + spacing
                page_path = out_path.with_name(f"{stem}_{file_number:03d}{suffix}")
                final_img.save(page_path, quality=92)
                saved_paths.append(page_path)

            self.export_progress_queue.put({
                "kind": "done",
                "path": str(saved_paths[0]) if saved_paths else str(out_path),
                "folder": str(out_path.parent),
                "csv_path": str(csv_path),
                "count": len(blocks),
                "file_count": len(saved_paths),
            })
        except Exception as exc:
            self.export_progress_queue.put({"kind": "error", "error": f"{exc}\n\n{traceback.format_exc(limit=4)}"})

    def _poll_export_progress_queue(self) -> None:
        try:
            while True:
                item = self.export_progress_queue.get_nowait()
                kind = item.get("kind")
                if kind == "progress":
                    done = int(item.get("done", 0))
                    total = int(item.get("total", 0))
                    record = int(item.get("record", 0))
                    self.status_var.set(f"Exporting selected records to JPG: {done}/{total} (record {record})…")
                elif kind == "done":
                    self.export_running = False
                    self.export_button.configure(state="normal")
                    path = str(item.get("path", ""))
                    folder = str(item.get("folder", ""))
                    count = int(item.get("count", 0))
                    file_count = int(item.get("file_count", 1))
                    csv_path = str(item.get("csv_path", ""))
                    self.status_var.set(f"Export complete. Saved {count} selected records in {file_count} JPG files and one CSV file: {folder}")
                    messagebox.showinfo(
                        "Export complete",
                        f"Saved {count} selected records in {file_count} JPG files and one CSV file.\n\n"
                        f"Folder:\n{folder}\n\nFirst JPG file:\n{path}\n\nCSV file:\n{csv_path}",
                    )
                elif kind == "error":
                    self.export_running = False
                    self.export_button.configure(state="normal")
                    error = str(item.get("error", "Unknown export error."))
                    self.status_var.set("Export error.")
                    messagebox.showerror("Export error", error)
        except queue.Empty:
            pass
        self.root.after(100, self._poll_export_progress_queue)

