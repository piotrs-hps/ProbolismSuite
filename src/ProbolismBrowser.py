def normalize_column_name(name: str) -> str:
    return str(name).strip().lstrip("\ufeff")


def format_float(value: Any, digits: int = 3, fallback: str = "—") -> str:
    try:
        if pd.isna(value):
            return fallback
        x = float(value)
        return f"{x:.{digits}f}"
    except Exception:
        text = str(value).strip()
        return text if text else fallback


def sexagesimal_pair_to_degrees(ra_value: Any, dec_value: Any) -> Tuple[float, float]:
    """Parse RA/Dec columns produced by the probolism-search program."""
    ra_s = str(ra_value).strip()
    dec_s = str(dec_value).strip()
    if not ra_s or not dec_s or ra_s.lower() == "nan" or dec_s.lower() == "nan":
        raise ValueError("Empty RA/Dec coordinates.")

    # 1) Decimal degrees in both columns.
    try:
        ra_float = float(ra_s)
        dec_float = float(dec_s)
        return ra_float % 360.0, dec_float
    except Exception:
        pass

    # 2) Strings like 12h34m56.7s and +12d34m56.7s from astropy.to_string().
    try:
        coord = SkyCoord(f"{ra_s} {dec_s}", unit=(u.hourangle, u.deg), frame="icrs")
        return coord.ra.deg % 360.0, coord.dec.deg
    except Exception:
        pass

    # 3) Strings like 188.7 +12.3 in a single-ish format, or RA written with spaces.
    try:
        coord = SkyCoord(f"{ra_s} {dec_s}", frame="icrs")
        return coord.ra.deg % 360.0, coord.dec.deg
    except Exception as exc:
        raise ValueError(f"Cannot parse coordinates: RA={ra_s!r}, DEC={dec_s!r}") from exc


def row_to_center_degrees(row: pd.Series) -> Tuple[float, float]:
    columns = {normalize_column_name(c): c for c in row.index}

    # Prefer J2000/ICRS center columns from the source program.
    for ra_col, dec_col in [
        ("center_RA", "center_DEC"),
        ("ra", "dec"),
        ("RA", "DEC"),
        ("ra_deg", "dec_deg"),
        ("RA_deg", "DEC_deg"),
        ("center_RA_current", "center_DEC_current"),
    ]:
        if ra_col in columns and dec_col in columns:
            return sexagesimal_pair_to_degrees(row[columns[ra_col]], row[columns[dec_col]])

    raise ValueError(
        "No coordinate column pair was found. Expected columns such as center_RA and center_DEC."
    )


def center_constellation_name(ra_deg: float, dec_deg: float) -> str:
    try:
        coord = SkyCoord(ra_deg * u.deg, dec_deg * u.deg, frame="icrs")
        return str(get_constellation(coord, short_name=False))
    except Exception as exc:
        return f"unknown ({exc})"


def split_galaxy_names(galaxy_text: Any) -> List[str]:
    if galaxy_text is None or pd.isna(galaxy_text):
        return []
    text = str(galaxy_text).strip()
    if not text or text.lower() == "nan":
        return []
    parts = re.split(r"\s*;\s*|\s*,\s*(?=[A-Za-z]{1,5}\s*\d)", text)
    names: List[str] = []
    for part in parts:
        part = part.strip()
        if part and part.lower() != "nan" and part not in names:
            names.append(part)
    return names


def safe_filename_hash(payload: Dict[str, Any]) -> str:
    stable = json.dumps(payload, ensure_ascii=False, sort_keys=True, separators=(",", ":"))
    return hashlib.sha256(stable.encode("utf-8")).hexdigest()[:24]


def build_hips2fits_url(
    endpoint: str,
    hips: str,
    ra_deg: float,
    dec_deg: float,
    fov_deg: float,
    width: int,
    height: int,
    image_format: str = "jpg",
) -> str:
    params = {
        "hips": hips,
        "width": int(width),
        "height": int(height),
        "projection": "TAN",
        "ra": f"{ra_deg:.9f}",
        "dec": f"{dec_deg:.9f}",
        "fov": f"{fov_deg:.6f}",
        "coordsys": "icrs",
        "rotation_angle": "0",
        "format": image_format,
        "min_cut": "0.5%",
        "max_cut": "99.5%",
        "stretch": "asinh",
    }
    return endpoint + "?" + urlencode(params)


def fetch_url_to_file(url: str, out_path: Path) -> None:
    out_path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = out_path.with_suffix(out_path.suffix + ".tmp")
    headers = {"User-Agent": "probolism-csv-browser/1.0"}
    with requests.get(url, timeout=REQUEST_TIMEOUT_SEC, headers=headers, stream=True) as response:
        response.raise_for_status()
        content_type = response.headers.get("content-type", "")
        if "text/html" in content_type.lower():
            # Usually means an error page instead of an image.
            text = response.text[:500]
            raise RuntimeError(f"The service returned HTML instead of an image: {text}")
        with tmp_path.open("wb") as f:
            for chunk in response.iter_content(chunk_size=1024 * 64):
                if chunk:
                    f.write(chunk)
    tmp_path.replace(out_path)


def is_valid_image_file(path: Path) -> bool:
    if not path.exists() or path.stat().st_size <= 0:
        return False
    try:
        with Image.open(path) as img:
            img.verify()
        return True
    except Exception:
        return False


def fetch_image_to_cache_with_retries(primary_url: str, mirror_url: str, image_path: Path) -> str:
    last_error = ""
    for attempt in range(1, BULK_DOWNLOAD_RETRIES + 1):
        for url in (primary_url, mirror_url):
            try:
                fetch_url_to_file(url, image_path)
                if is_valid_image_file(image_path):
                    return url
                last_error = "Downloaded file is not a readable image."
                try:
                    image_path.unlink()
                except Exception:
                    pass
            except Exception as exc:
                last_error = str(exc)
                try:
                    if image_path.exists() and not is_valid_image_file(image_path):
                        image_path.unlink()
                except Exception:
                    pass
        if attempt < BULK_DOWNLOAD_RETRIES:
            time.sleep(BULK_RETRY_DELAY_SEC * attempt)
    raise RuntimeError(last_error or "Image download failed.")


def angular_separation_deg(ra1: float, dec1: float, ra2: float, dec2: float) -> float:
    c1 = SkyCoord(ra1 * u.deg, dec1 * u.deg, frame="icrs")
    c2 = SkyCoord(ra2 * u.deg, dec2 * u.deg, frame="icrs")
    return float(c1.separation(c2).deg)


class GalaxyResolver:
    """Small persistent cache around SkyCoord.from_name/Sesame resolution."""

    def __init__(self, cache_dir: Path):
        self.cache_dir = cache_dir
        self.cache_path = cache_dir / "galaxy_name_resolutions.json"
        self._data: Dict[str, Any] = {}
        self._load()

    def _load(self) -> None:
        try:
            if self.cache_path.exists():
                self._data = json.loads(self.cache_path.read_text(encoding="utf-8"))
        except Exception:
            self._data = {}

    def _save(self) -> None:
        try:
            self.cache_dir.mkdir(parents=True, exist_ok=True)
            self.cache_path.write_text(
                json.dumps(self._data, ensure_ascii=False, indent=2, sort_keys=True),
                encoding="utf-8",
            )
        except Exception:
            pass

    @staticmethod
    def _variants(name: str) -> List[str]:
        name = name.strip()
        variants = [name]
        # Improve common RC3 forms such as NGC4440, IC123, PGC12345.
        m = re.match(r"^(NGC|IC|PGC|UGC|MCG)(\d.*)$", name, flags=re.IGNORECASE)
        if m:
            variants.append(f"{m.group(1).upper()} {m.group(2).strip()}")
        variants.append(name.replace("_", " "))
        out: List[str] = []
        for v in variants:
            v = re.sub(r"\s+", " ", v).strip()
            if v and v not in out:
                out.append(v)
        return out

    def resolve(self, name: str) -> Optional[Tuple[float, float]]:
        key = re.sub(r"\s+", " ", name.strip())
        if not key:
            return None
        cached = self._data.get(key)
        if isinstance(cached, dict) and cached.get("ok"):
            return float(cached["ra_deg"]), float(cached["dec_deg"])
        if isinstance(cached, dict) and cached.get("ok") is False:
            return None

        for variant in self._variants(key):
            try:
                coord = SkyCoord.from_name(variant, parse=True)
                ra_dec = (float(coord.ra.deg) % 360.0, float(coord.dec.deg))
                self._data[key] = {"ok": True, "name_used": variant, "ra_deg": ra_dec[0], "dec_deg": ra_dec[1]}
                self._save()
                return ra_dec
            except Exception:
                time.sleep(0.15)

        self._data[key] = {"ok": False, "failed_at": time.strftime("%Y-%m-%dT%H:%M:%S")}
        self._save()
        return None


